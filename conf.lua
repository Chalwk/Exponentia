function love.conf(t)
    t.window.title = "Exponentia - Power of Two"
    t.window.width = 600
    t.window.height = 700
    t.window.minwidth = 600
    t.window.minheight = 700
    t.window.fullscreen = false
    t.window.resizable = true
    t.console = true
end